# in operators check if an element is present

# example 1 [List]

fruits = ["apple", "banana", "mango"]
print("banana" in fruits)

# example 2 

fruits = ["apple", "banana", "mango"]
print("orange" in fruits)

# example 1 (strings)

lang = "python"
print("p" in lang)
print("java" in lang)


# not in operators check if an element is not present

fruits = ["apple", "banana", "mango"]
print("orange" not in fruits)
print("banana" not in fruits)

