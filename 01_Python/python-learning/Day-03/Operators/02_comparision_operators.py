a = 10
b = 5

print("a =",a)
print("b =",b)

print("Equal:", a == b)          # Equal    
print("Not Equal:", a != b)      # Not Equal    
print("Greater than:", a > b)    # Greater than
print("Less than:", a < b)       # Less than
print("Greater than or equal:", a >= b)  # Greater than or equal
print("Less than or equal:", a <= b)    # Less than or equal   


# Equal (==) operator checks if two values are equal and returns True or False.
print(10 == 10)  # True
print(10 == 5)   # False

# Not Equal (!=) operator checks if two values are not equal and returns True or False.
print(10 != 5)   # True
print(10 != 10)  # False

# Greater than (>) operator checks if the left value is greater than the right value and returns True or False.
print(10 > 5)    # True 
print(5 > 10)    # False

# Less than (<) operator checks if the left value is less than the right value and returns True or False.
print(5 < 10)    # True 
print(10 < 5)    # False

# Greater than or equal (>=) operator checks if the left value is greater than or equal to the right value and returns True or False.
print(10 >= 5)   # True
print(5 >= 10)   # False

# Less than or equal (<=) operator checks if the left value is less than or equal to the right value and returns True or False.
print(5 <= 10)    # True
print(10 <= 5)    # False

