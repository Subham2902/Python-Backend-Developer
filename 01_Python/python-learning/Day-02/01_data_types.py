# Strings(str)

name = "Subham"
print(name)
print(type(name))

# Boolean(bool)

is_student = True
is_logged_in = False
print(type(is_student))
print(type(is_logged_in))

# Complex(complex)

a = 10 + 5j
print(a)
print(type(a))

# List(list)

languages = ["Python", "SQL", "Java"]
print(languages)
print(type(languages))

# Tuple(tuple)

numbers = (10, 20, 30)
print(numbers)
print(type(numbers))

# Set(set)  

fruits = {"Apple", "Banana", "Apple"}
print(fruits)
print(type(fruits))

# Dictionary(dict)

student = {
    "name": "Subham",
    "age": 23
}
print(student)
print(type(student))


